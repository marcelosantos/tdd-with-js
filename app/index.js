function add(a,b)
{
    return a + b;
}

function sub(a,b)
{
    return a - b;
}

function div(a,b)
{
    return a / b;
}

function mul(a, b)
{
    return a * b;
}

function exp(a, b)
{
    return Math.pow(a, b);
}

function sqr(a)
{
    return Math.sqrt(a);
}

function avg(a,b)
{
    return (a + b) / 2;
}
